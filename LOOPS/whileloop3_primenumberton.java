// prime number 1 to n value 

import java.util.*;

public class whileloop3_primenumberton {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int N = sc.nextInt();
        int counter = 1;
        while (counter <= N) {
            System.out.println(counter);
            counter++;

        }
    }

}
