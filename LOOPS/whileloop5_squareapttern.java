
public class whileloop5_squareapttern {

    public static void main(String args[]) {
        int line = 1;
        while (line <= 4) {
            int i = 1;
            while (i <= 4) {
                System.out.print("*");
                i++;
            }

            System.out.println();
            line++;
        }
    }

}
